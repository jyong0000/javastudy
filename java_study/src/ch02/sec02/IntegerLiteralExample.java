package ch02.sec02;

public class IntegerLiteralExample {

	public static void main(String[] args) {
		int val1 = 0b1011;
		int val2 = 0206;
		int val3 = 365;
		int val4 = 0xB3;
		
		// 컨트롤 + D = 라인삭제
		// 컨트롤 + 알트 + 방향키 = 라인복사
		System.out.println("val1: " + val1);
		System.out.println("val2: " + val2);
		System.out.println("val3: " + val3);
		System.out.println("val4: " + val4);

	}

}
